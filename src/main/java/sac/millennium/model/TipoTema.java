package sac.millennium.model;

public class TipoTema {

	private String idTipoTema;
	private String nombreTipoTema;
	private String siglasTipoTema;

	public TipoTema() {

	}

	public String getIdTipoTema() {
		return idTipoTema;
	}

	public void setIdTipoTema(String idTipoTema) {
		this.idTipoTema = idTipoTema;
	}

	public String getNombreTipoTema() {
		return nombreTipoTema;
	}

	public void setNombreTipoTema(String nombreTipoTema) {
		this.nombreTipoTema = nombreTipoTema;
	}

	public String getSiglasTipoTema() {
		return siglasTipoTema;
	}

	public void setSiglasTipoTema(String siglasTipoTema) {
		this.siglasTipoTema = siglasTipoTema;
	}

}
